import React, {Component} from 'react';
import {View, Text, Image, TouchableOpacity} from 'react-native';
import PropTypes from 'prop-types';
import {ParallaxImage} from 'react-native-snap-carousel';
// import styles from '../styles/SliderEntry.style';
import {StyleSheet, Dimensions, Platform} from 'react-native';
// import { colors } from './index.style';
import {NavigationService} from '../services';
// import {Tile} from 'react-native-elements';

type SliderEntryType = {
  data: any,
  even: boolean,
  parallax: boolean,
  parallaxProps: any,
  onSlidePress: (slide : SliderEntryType) => void,
}

const IS_IOS = Platform.OS === 'ios';
const {width: viewportWidth, height: viewportHeight} = Dimensions.get('window');

const colors = {
  black: '#1a1917',
  gray: '#888888',
  background1: '#B721FF',
  background2: '#21D4FD',
};

function wp(percentage) {
  const value = (percentage * viewportWidth) / 100;
  return Math.round(value);
}

const slideHeight = viewportHeight * 0.36;
const slideWidth = wp(75);
const itemHorizontalMargin = wp(2);

export const sliderWidth = viewportWidth;
export const itemWidth = slideWidth + itemHorizontalMargin * 2;

const entryBorderRadius = 8;

export default class SliderEntry extends Component<SliderEntryType> {

  // static propTypes = {
  //   data: PropTypes.object.isRequired,
  //   even: PropTypes.bool,
  //   parallax: PropTypes.bool,
  //   parallaxProps: PropTypes.object,
  // };

  get image() {
    const {
      data: {illustration, image},
      parallax,
      parallaxProps,
      even,
    } = this.props;

    return parallax ? (
      <ParallaxImage
        source={{uri: image ? image.src : illustration}}
        containerStyle={[
          styles.imageContainer,
          even ? styles.imageContainerEven : {},
        ]}
        style={styles.image}
        parallaxFactor={0.35}
        showSpinner={true}
        spinnerColor={even ? 'rgba(255, 255, 255, 0.4)' : 'rgba(0, 0, 0, 0.25)'}
        {...parallaxProps}
      />
    ) : (
      <Image
        source={{uri: image ? image.src : illustration}}
        style={styles.image}
      />
    );
  }

  render() {
    const {
      data: {title, subtitle, tag_ids},
      even,
    } = this.props;

    const uppercaseTitle = title ? (
      <Text
        style={[styles.title, even ? styles.titleEven : {color: 'black'}]}
        numberOfLines={2}>
        {title.toUpperCase()}
      </Text>
    ) : (
      false
    );

    return (
      <TouchableOpacity
        activeOpacity={1}
        style={styles.slideInnerContainer}
        onPress={() => {
          NavigationService.navigateToSearchResult({
            title: title,
            options: {tag_ids: tag_ids},
          });
        }}>
        <View style={styles.shadow} />
        <View
          style={[
            styles.imageContainer,
            even ? styles.imageContainerEven : {},
          ]}>
          {this.image}
          <View
            style={[styles.radiusMask, even ? styles.radiusMaskEven : {}]}
          />
        </View>
        <View
          style={[styles.textContainer, even ? styles.textContainerEven : {}]}>
          {uppercaseTitle}
          <Text
            style={[
              styles.subtitle,
              even ? styles.subtitleEven : {color: 'black'},
            ]}
            numberOfLines={2}>
            {subtitle}
          </Text>
        </View>
      </TouchableOpacity>
    );
  }
}

